        items_list = ['/avnotes/', '/tracks/rec/', '/favorites/', '/itinerary.gpx']
        os.makedirs(tmp_dir_name + items_list[0])
        os.makedirs(tmp_dir_name + items_list[1])
        os.makedirs(tmp_dir_name + items_list[2])

