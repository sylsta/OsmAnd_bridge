                    device_model_name = str(device_open.get_model_name())

                    if self.cBdeviceList.currentText() == (f'{device_model_name} - {str(device_open)[9:-2]}'):
                        print(f'Looking for osmand files on {device_model_name} - {str(device_open)[9:-2]}')


